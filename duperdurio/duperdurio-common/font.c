/* Ё = Е with dots */
static const unsigned char glyph_yo[7] =
    {0x0A,0x1F,0x10,0x1E,0x10,0x10,0x1F};

/* ═══════════════════ Glyph lookup ═══════════════════ */

const unsigned char *font_glyph(const char **str) {
    const unsigned char *s = (const unsigned char *)*str;
    if (*s == 0) return NULL;

    /* ASCII */
    if (*s < 0x80) {
        unsigned char ch = *s;
        (*str)++;
        if (ch < 32 || ch > 126) return ascii_font[0]; /* space fallback */
        return ascii_font[ch - 32];
    }

    /* UTF-8 two-byte: Cyrillic */
    if (*s == 0xD0 && s[1]) {
        unsigned char b = s[1];
        *str += 2;
        if (b == 0x81)             return glyph_yo;           /* Ё */
        if (b >= 0x90 && b <= 0xAF) return cyrillic_font[b - 0x90]; /* А-Я */
        if (b >= 0xB0 && b <= 0xBF) return cyrillic_font[b - 0xB0]; /* а-п → А-П */
        return ascii_font['?' - 32];
    }
    if (*s == 0xD1 && s[1]) {
        unsigned char b = s[1];
        *str += 2;
        if (b == 0x91)             return glyph_yo;           /* ё */
        if (b >= 0x80 && b <= 0x8F) return cyrillic_font[b - 0x80 + 16]; /* р-я → Р-Я */
        return ascii_font['?' - 32];
    }

    /* Unknown multi-byte: skip */
    (*str)++;
    return ascii_font['?' - 32];
}

/* ═══════════════════ String width ═══════════════════ */

int font_string_width(const char *str) {
    int w = 0;
    while (*str) {
        font_glyph(&str);
        if (w > 0) w += FONT_SPACING;
        w += FONT_W;
    }
    return w;
}

/* ═══════════════════ Framebuffer renderer (NDS) ═══════════════════ */

void font_draw_string_fb(uint16_t *fb, int fb_stride, int fb_h,
                         int x, int y, const char *str,
                         uint16_t color, uint16_t alpha_bit) {
    uint16_t c = color | alpha_bit;
    int cx = x;
    while (*str) {
        const unsigned char *g = font_glyph(&str);
        if (!g) break;
        for (int r = 0; r < FONT_H; r++) {
            int sy = y + r;
            if ((unsigned)sy >= (unsigned)fb_h) continue;
            unsigned char row = g[r];
            for (int col = 0; col < FONT_W; col++) {
                if (!(row & (1 << (4 - col)))) continue;
                int sx = cx + col;
                if ((unsigned)sx >= (unsigned)fb_stride) continue;
                fb[sy * fb_stride + sx] = c;
            }
        }
        cx += FONT_W + FONT_SPACING;
    }
}

/* ═══════════════════ Run-based renderer (3DS) ═══════════════════ */

void font_draw_string_cb(int x, int y, const char *str,
                         font_run_cb cb, void *ctx) {
    int cx = x;
    while (*str) {
        const unsigned char *g = font_glyph(&str);
        if (!g) break;
        for (int r = 0; r < FONT_H; r++) {
            unsigned char row = g[r];
            if (row == 0) continue;
            /* Find horizontal runs of consecutive set bits */
            int col = 0;
            while (col < FONT_W) {
                if (!(row & (1 << (4 - col)))) { col++; continue; }
                int start = col;
                while (col < FONT_W && (row & (1 << (4 - col)))) col++;
                cb(cx + start, y + r, col - start, ctx);
            }
        }
        cx += FONT_W + FONT_SPACING;
    }
}
